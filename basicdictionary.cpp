QString basicDictionary::getRandomWord(int n, QList<QString> originalText)
{
    QString result;
    int random;
    int i;
    bool repeat = false;
    while(true)
    {
        //generate random number
        repeat = false;
        random = qrand() % ((999 + 1) - 0) + 0;
        qDebug() << "Random number generated: " << random;

        //check against originalText; reject and regenerate random number if present
        for (i = 0 ; i < n; i++ )
        {
            if (dictionary[random] == originalText[i])
            {
                //repeat found
                repeat = true;
            }
        }

        //if word is unique, then send back the number
        if (!repeat)
        {
            break;
        }
    }
    result = dictionary[random];
    return result;
}

QString basicDictionary::getAWord(int n)
{
    return (dictionary[n]);
}

QList<QString> basicDictionary::getWordList(QString originalText, int n)
{
    QList <QString> result;
    QString string;
    result.push_back(originalText);
    for (int i = 1; i <= n; i++ )
    {
        string = getRandomWord(i,result);
        result.push_back(string);
    }
    return result;
}
